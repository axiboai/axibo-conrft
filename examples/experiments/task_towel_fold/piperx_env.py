"""Gym interface for a bimanual PiperX robot (towel folding).

This mirrors ``franka_env.FrankaEnv`` but for a 14-DoF bimanual joint-control
robot, producing observations that match the demos generated by
``convert_lerobot_to_conrft.py``:

    observations = {
        "state": {"joint_pos": (14,)},          # 12 joints + 2 grippers
        "images": {"side_policy_256": (256,256,3), "wrist_1": (128,128,3)},
    }
    action_space = Box(-1, 1, shape=(14,))      # normalized joint targets

HARDWARE INTEGRATION
--------------------
This file talks to a robot HTTP server (like ``franka_server.py``) via the
``SERVER_URL``. You must provide a server exposing these endpoints:

    POST /getstate   -> {"joint_pos": [14 floats]}
    POST /command    -> body {"joint_pos": [14 floats]}   (absolute joint targets)
    POST /reset      -> move to the home/reset pose

Search for ``# TODO(piperx)`` for the exact spots to adapt to your robot SDK.
When ``fake_env=True`` (used by the learner) no hardware is contacted.
"""

import time
import copy
import queue
import threading
from collections import OrderedDict
from typing import Dict

import cv2
import numpy as np
import requests
import gymnasium as gym


class PiperXEnvConfig:
    """Fill these in for your PiperX workspace."""

    SERVER_URL: str = "http://127.0.0.1:5000/"

    # name -> arbitrary capture kwargs for your camera backend.
    CAMERAS: Dict[str, dict] = {
        "side_policy_256": {"index": 0},
        "wrist_1": {"index": 1},
    }
    # Optional per-camera crop callables applied before resize.
    IMAGE_CROP: Dict[str, callable] = {}

    STATE_DIM: int = 14
    ACTION_DIM: int = 14

    # Reset joint configuration (absolute joint targets) the robot returns to.
    RESET_JOINTS: np.ndarray = np.zeros((14,), dtype=np.float32)

    # Normalized action -> joint delta scaling (per joint). Actions are clipped
    # to [-1, 1] then multiplied by ACTION_SCALE and added to current joints.
    ACTION_SCALE: np.ndarray = np.full((14,), 0.05, dtype=np.float32)

    # Hard joint limits for safety clipping (absolute targets).
    JOINT_LIMIT_LOW: np.ndarray = np.full((14,), -np.pi, dtype=np.float32)
    JOINT_LIMIT_HIGH: np.ndarray = np.full((14,), np.pi, dtype=np.float32)

    DISPLAY_IMAGE: bool = True
    MAX_EPISODE_LENGTH: int = 200
    CONTROL_HZ: float = 10.0


class ImageDisplayer(threading.Thread):
    def __init__(self, q, name):
        threading.Thread.__init__(self)
        self.queue = q
        self.daemon = True
        self.name = name

    def run(self):
        while True:
            img_array = self.queue.get()
            if img_array is None:
                break
            frame = np.concatenate(
                [cv2.resize(v, (128, 128)) for v in img_array.values()], axis=1)
            cv2.imshow(self.name, frame)
            cv2.waitKey(1)


class PiperXEnv(gym.Env):
    def __init__(self, fake_env=False, save_video=False, config: PiperXEnvConfig = None):
        self.config = config or PiperXEnvConfig()
        self.url = self.config.SERVER_URL
        self.max_episode_length = self.config.MAX_EPISODE_LENGTH
        self.hz = self.config.CONTROL_HZ
        self.display_image = self.config.DISPLAY_IMAGE
        self.save_video = save_video

        self.action_space = gym.spaces.Box(
            -np.ones((self.config.ACTION_DIM,), dtype=np.float32),
            np.ones((self.config.ACTION_DIM,), dtype=np.float32),
        )
        self.observation_space = gym.spaces.Dict({
            "state": gym.spaces.Dict({
                "joint_pos": gym.spaces.Box(-np.inf, np.inf,
                                            shape=(self.config.STATE_DIM,)),
            }),
            "images": gym.spaces.Dict({
                "side_policy_256": gym.spaces.Box(0, 255, shape=(256, 256, 3), dtype=np.uint8),
                "wrist_1": gym.spaces.Box(0, 255, shape=(128, 128, 3), dtype=np.uint8),
            }),
        })

        self.currpos = self.config.RESET_JOINTS.copy()
        self.curr_path_length = 0
        self.terminate = False
        self.recording_frames = []

        if fake_env:
            return

        self.cap = None
        self.init_cameras(self.config.CAMERAS)
        if self.display_image:
            self.img_queue = queue.Queue()
            self.displayer = ImageDisplayer(self.img_queue, self.url)
            self.displayer.start()

        from pynput import keyboard
        def on_press(key):
            if key == keyboard.Key.esc:
                self.terminate = True
        self.listener = keyboard.Listener(on_press=on_press)
        self.listener.start()
        print("Initialized PiperX env")

    # ----------------------------------------------------------------- hardware
    def init_cameras(self, name_kwargs):
        # TODO(piperx): replace cv2.VideoCapture with your camera backend if needed.
        if self.cap is not None:
            self.close_cameras()
        self.cap = OrderedDict()
        for name, kwargs in name_kwargs.items():
            self.cap[name] = cv2.VideoCapture(kwargs.get("index", 0))

    def close_cameras(self):
        try:
            for cap in self.cap.values():
                cap.release()
        except Exception as e:
            print(f"Failed to close cameras: {e}")

    def _get_state(self):
        # TODO(piperx): adapt to your server's state endpoint.
        ps = requests.post(self.url + "getstate").json()
        self.currpos = np.asarray(ps["joint_pos"], dtype=np.float32)
        return self.currpos

    def _send_command(self, joint_targets):
        # TODO(piperx): adapt to your server's command endpoint.
        joint_targets = np.clip(joint_targets,
                                self.config.JOINT_LIMIT_LOW,
                                self.config.JOINT_LIMIT_HIGH)
        requests.post(self.url + "command",
                      json={"joint_pos": np.asarray(joint_targets, dtype=np.float32).tolist()})

    def _server_reset(self):
        # TODO(piperx): adapt to your server's reset endpoint.
        requests.post(self.url + "reset",
                      json={"joint_pos": self.config.RESET_JOINTS.tolist()})

    def get_im(self) -> Dict[str, np.ndarray]:
        images, display_images = {}, {}
        for key, cap in self.cap.items():
            ok, rgb = cap.read()
            if not ok:
                input(f"{key} camera frozen. Reconnect and press enter...")
                self.init_cameras(self.config.CAMERAS)
                return self.get_im()
            rgb = rgb[..., ::-1]  # BGR -> RGB
            if key in self.config.IMAGE_CROP:
                rgb = self.config.IMAGE_CROP[key](rgb)
            target = self.observation_space["images"][key].shape[:2][::-1]
            resized = cv2.resize(rgb, target)
            images[key] = resized.astype(np.uint8)
            display_images[key] = resized
        self.recording_frames.append(copy.deepcopy(images))
        if self.display_image:
            self.img_queue.put(display_images)
        return images

    # ----------------------------------------------------------------- gym API
    def _get_obs(self):
        images = self.get_im()
        return copy.deepcopy(dict(
            images=images,
            state={"joint_pos": self.currpos.astype(np.float32)},
        ))

    def compute_reward(self, obs) -> bool:
        # Real success signal comes from the reward-classifier wrapper.
        return False

    def step(self, action: np.ndarray):
        start = time.time()
        action = np.clip(action, self.action_space.low, self.action_space.high)
        target = self.currpos + action * self.config.ACTION_SCALE
        self._send_command(target)

        self.curr_path_length += 1
        time.sleep(max(0.0, (1.0 / self.hz) - (time.time() - start)))

        self._get_state()
        obs = self._get_obs()
        reward = self.compute_reward(obs)
        done = (self.curr_path_length >= self.max_episode_length
                or bool(reward) or self.terminate)
        return obs, int(reward), done, False, {"succeed": bool(reward)}

    def reset(self, **kwargs):
        self._server_reset()
        time.sleep(1.0)
        self.curr_path_length = 0
        self.terminate = False
        self._get_state()
        obs = self._get_obs()
        return obs, {"succeed": False}

    def close(self):
        if hasattr(self, "listener"):
            self.listener.stop()
        if getattr(self, "cap", None) is not None:
            self.close_cameras()
        if self.display_image and hasattr(self, "img_queue"):
            self.img_queue.put(None)
            cv2.destroyAllWindows()
